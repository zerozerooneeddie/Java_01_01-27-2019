public class ForLoop{
		public static void main(String[] args) 
		{

				//Nested loop//
			//Displaying int triangle///

				for (int i = 0; i< 5; i++)
				{

					for (int j = 0; j<=i; j++)
					{

						//if (j> i) break;//

					System.out.print('*');

				}

					System.out.println(" ");
				}


					



		}


}